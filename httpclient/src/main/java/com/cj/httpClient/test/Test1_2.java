        Double targetNum = 0.0;
        HashSet<Set<Double>> resultSet = new HashSet<>();

        for (Double sourceInt : sourceSet) {
            Double temp = targetNum - sourceInt;
            if (sourceSet.contains(temp)) {
                HashSet<Double> subSet = new HashSet<>();
                subSet.add(sourceInt);
                subSet.add(temp);
                resultSet.add(subSet);
            }
        }

